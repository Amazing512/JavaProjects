    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(veiExcluirAlterar == null)
        {
            JOptionPane.showMessageDialog(this, "Escolha um veiculo na tabela!", "Erro", HEIGHT);
        }
        else
        {
            try {
                veiCtrl.excluirVeiculo(veiExcluirAlterar.getPlaca());
                JOptionPane.showMessageDialog(this, "Excluido com sucesso!", "Sucesso!", HEIGHT);
                btnPesquisarActionPerformed(evt);
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Ocorreu um erro, por favor tente novamente!", "Erro", HEIGHT);
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if(veiExcluirAlterar == null)
        {
            JOptionPane.showMessageDialog(this, "Escolha um veiculo na tabela!", "Erro", HEIGHT);
        }
        else
        {
            FrmAlterar.getInstanceOfFrmAlterar(veiExcluirAlterar).setVisible(true);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

