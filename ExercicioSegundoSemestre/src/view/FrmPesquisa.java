    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        int indexComboBox = this.cbbTipoPesquisa.getSelectedIndex();
        veiExcluirAlterar = null;
        try
        {
            DefaultTableModel dados = (DefaultTableModel) tblVeiculos.getModel(); 
            dados.setNumRows(0);
            ArrayList<Veiculo> array = veiCtrl.pesquisarVeiculo(txtParametroPesquisa.getText(),indexComboBox);
            
            Iterator it = array.iterator();
            while(it.hasNext())
            {
                Veiculo v = (Veiculo) it.next();
                dados.addRow(new Object[]{v.getPlaca(),v.getMarca(),v.getModelo(),v.getCor(),v.getAnoFabricacao()});
            }
                    
        }
        catch(SQLException | NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro, verifique se voce colocou o dado corretamente!!", "Erro", HEIGHT);
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro, por favor tente novamente!", "Erro", HEIGHT);
        }

    }//GEN-LAST:event_btnPesquisarActionPerformed

