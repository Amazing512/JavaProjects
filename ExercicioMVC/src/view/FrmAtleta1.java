    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        try
        {
            String nome = this.txtNome.getText();
            int idade = Integer.parseInt(this.txtIdade.getText());
            int cpf = Integer.parseInt(this.txtCPF.getText());
            double altura = Double.parseDouble(this.txtAltura.getText());
            double peso = Double.parseDouble(this.txtPeso.getText());
            atCtrl.cadastrarAtleta(nome, cpf, idade, altura, peso);
            JOptionPane.showMessageDialog(null,"Atleta cadastrado com sucesso!");
            limparDadosCadastro();
        }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"Passe apenas nÃºmeros como valor (exceto em nome)");
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        ArrayList<Atleta> lista = atCtrl.buscarAtleta(txtCPFBuscarExcluir.getText());
        String result = "";
        try{
            if("".equals(txtCPFBuscarExcluir.getText().trim()))
            {
                for(Atleta a: lista)
                {
                    result = result.concat("\nCPF: " + a.getCpf() + ", Nome: " + a.getNome());
                }
            }
            else
            {
                int cpf = Integer.parseInt(txtCPFBuscarExcluir.getText()); // se der erro NumberFormatException
                for(Atleta a: lista)
                {
                    result = result.concat("\nCPF: " + a.getCpf() + ", Nome: " + 
                            a.getNome()+ ", Idade: " + a.getIdade()+ ", Altura: "+ a.getAltura() + ", Peso:" + a.getPeso() );
                }
            }
            JOptionPane.showMessageDialog(null,result);
        }catch(NullPointerException e){
            JOptionPane.showMessageDialog(null,"Nenhum resultado encontrado.");
        }
        catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null,"Digite um CPF vÃ¡lido.");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Erro.");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        

        boolean excluido;
        if("".equals(txtCPFBuscarExcluir.getText().trim()))
        {
            JOptionPane.showMessageDialog(null,"Insira o CPF do atleta para realizar a exclusÃ£o.");
        }
        else
        {
            try{
                int cpfAtleta = Integer.parseInt(txtCPFBuscarExcluir.getText());
                
                excluido = atCtrl.excluirAtleta(cpfAtleta);
                if(excluido)
                {
                    JOptionPane.showMessageDialog(null,"Excluido com sucesso!");
                }
                else
                {
                    JOptionPane.showMessageDialog(null,"NÃ£o existe atleta com esse CPF!");
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,"Insira um CPF VÃ¡lido!");
            }
            finally
            {
                limparCampoPesquisa();
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnCalcularMaisAltoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularMaisAltoActionPerformed
        try
        {
            if(listaEstaVazia())
            {
                JOptionPane.showMessageDialog(null,"Nenhum atleta cadastrado."); 
            }
            else
            {
            Atleta a = atCtrl.calcularMaisAlto();
            JOptionPane.showMessageDialog(null,"Atleta mais alto:\nCPF: " + a.getCpf() + ", Nome: " + 
                            a.getNome()+ ", Idade: " + a.getIdade()+ ", Altura: "+ a.getAltura() + 
                    "cm, Peso:" + a.getPeso());
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Erro.");
        }
        
    }//GEN-LAST:event_btnCalcularMaisAltoActionPerformed

